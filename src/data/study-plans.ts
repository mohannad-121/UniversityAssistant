export function getMajorName(plan: StudyPlan, language: "en" | "ar") {
  return language === "ar" ? majorArabicNames[plan.id] : plan.name;
}

export function getCategoryName(category: StudyPlanCategory, language: "en" | "ar") {
  return language === "ar" ? categoryArabicNames[category] : category;
}

export function getCourseName(course: Course, language: "en" | "ar") {
  return language === "ar" ? courseArabicNames[course.code] ?? repairMojibake(course.name) : course.name;
}

export function findStudyPlanByName(name: string) {
  const normalized = name.toLowerCase();
  return studyPlans.find(
    (plan) =>
      plan.name.toLowerCase() === normalized ||
      (plan.id === "ai" && normalized.includes("artificial intelligence")) ||
      (plan.id === "cs" && normalized.includes("computer science")) ||
      (plan.id === "cybersecurity" && normalized.includes("cyber")) ||
      (plan.id === "software-engineering" && normalized.includes("software")),
  );
}

export function getStudyPlanCourses(plan: StudyPlan) {
  return plan.categories.flatMap((category) =>
    category.courseCodes.map((code) => ({
      category,
      course: studyPlanCourseMap.get(code),
    })),
  );
}

function course(
  code: string,
  name: string,
  department: string,
  creditHours: number,
  prerequisites: string[] = [],
): Course {
  return {
    id: code,
    code,
    name,
    department,
    creditHours,
    prerequisites,
    description: `${name} is a ${creditHours}-credit course in the ${department} study plan area.`,
  };
}
